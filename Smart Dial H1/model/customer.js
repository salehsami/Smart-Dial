const mongoose = require('mongoose');

const customerSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  contact: {
    type: String,
    required: true,
  },
  address1: {
    type: String,
    required: true,
  },
  address2: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    enum: ['Customer service', 'Technical support', 'Sales and marketing'],
    default: 'Customer service',
  },
});

const Customer = mongoose.model('Customer', customerSchema);

module.exports = Customer;